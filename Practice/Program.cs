using System;

namespace Practice
{
    public class Program
    {
        static void Main(string[] args)
        {

            /* Comparison Operators */

            // ( == ) Equal to
            int number1 = 5;
            int number2 = 3;
            Console.WriteLine(number1 == number2); // output false

            // ( != ) Not equal
            int number3 = 5;
            int number4 = 3;
            Console.WriteLine(number3 != number4); // output true

            // ( > ) Greater than
            int number5 = 5;
            int number6 = 3;
            Console.WriteLine(number5 > number6); // output true

            // ( < ) Less than
            int number7 = 5;
            int number8 = 3;
            Console.WriteLine(number8 < number7); // output false

            // ( >= ) Greater than or equal to
            int number9 = 5;
            int number10 = 3;
            Console.WriteLine(number9 >= number10); // output true

            // ( <= ) Less than or equal to
            int number11 = 5;
            int number12 = 3;
            Console.WriteLine(number11 <= number12); // output false








            Console.ReadLine();
        }
    }
}





